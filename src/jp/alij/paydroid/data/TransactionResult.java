package jp.alij.paydroid.data;

public class TransactionResult {

	public String msg;
	public int state ;
	
	
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	} 
}
